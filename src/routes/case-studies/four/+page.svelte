    <h2>Overview</h2>
    
    <p>The account features an e-shop specializing in Over-the-Counter (OTC) products. However, sales from Google Ads and Facebook Ads remained disappointingly minimal, significantly falling short of ad spend expectations. This compelled the client to part ways with their advertising agency and seek the expertise of a seasoned Paid Search freelancer.</p>
    
    <!-- The Challenge -->
    <h2>The Challenge</h2>
    <p>The primary challenge was the inadequate returns on advertising investment from both Google Ads and Facebook Ads. Consequently, the decision was made to terminate the services of the advertising agency and search for a specialized freelancer with expertise in Paid Search.</p>
    
    <!-- Unlocking Success: A Data-Driven Approach -->
    <h2>Unlocking Success: A Data-Driven Approach</h2>
    <p>To overcome the existing challenges, we adopted a data-driven approach to revamp sales and optimize advertising expenditure. The focus was on the two primary conversion points â€“ the e-shop and phone orders. While e-shop conversions could be directly compared to ad spend, phone orders were labeled as 'direct' in Google Analytics, necessitating meticulous attention.</p>
    
    <!-- Outstanding Results -->
    <h2>Outstanding Results</h2>
    <p>Our strategic efforts bore fruitful outcomes, delivering remarkable improvements:</p>
    <ul>
        <li>49.13% Increase in Revenue: A substantial surge in revenue, highlighting the potency of our tailored marketing approach.</li>
        <li>46.24% Growth in Transactions: Reinforcing the seamless connection between strategic planning and successful execution.</li>
        <li>62.01% Growth in Revenue per User: A testament to the power of personalized experiences and customer engagement.</li>
    </ul>
    
    <!-- Project Overview -->
    <h2>Project Overview</h2>
    <p>Our primary objective was to boost sales, with a specific emphasis on Google Ads, allocating a greater share compared to Facebook Ads. We ensured seamless data integration between Google Ads, Google Analytics, and Facebook pixel using Google Tag Manager, guaranteeing precise and reliable reporting. However, certain challenges surfaced with Facebook's product catalog, requiring prompt resolution.</p>
    
    <!-- Our Strategic Approach -->
    <h2>Our Strategic Approach</h2>
    <p>To achieve exceptional results, we embraced a versatile multi-channel approach, leveraging placements across Google, YouTube, Facebook, and Instagram. The media mix predominantly featured 95% static images and 5% engaging video content, effectively resonating with our target audience. Additionally, Paid Search played a pivotal role in driving the account's resounding success.</p>
    
