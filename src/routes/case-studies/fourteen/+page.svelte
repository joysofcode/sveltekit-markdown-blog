    <h2>Challenge</h2>
    <p>In a departure from the ordinary, Case Study 14 unveils the transformation journey of a client who owns and operates a singular medical spa. Situated on the outskirts of the city, the spa's strategic location posed visibility and discoverability challenges. Faced with this backdrop, the owner embarked on an endeavor to amplify leads by leveraging digital advertising. A prior collaboration with an established digital advertising agency failed to yield sustainable results, leading to a pivotal decision to seek alternative avenues. It was at this juncture that I stepped in.</p>

    <h2>Monthly Ad Spend Budget: $3K</h2>

    <h2>Results</h2>
    <ul>
        <li>An impressive <strong>206.67%</strong> surge in conversions resonated with newfound vitality.</li>
        <li>A noteworthy reduction of <strong>89.06%</strong> in Cost Per Acquisition (CPA) signaled a paradigm shift in efficiency.</li>
    </ul>

    <h2>Project Overview</h2>
    <p>The project was characterized by a dual objective: igniting lead generation while maintaining a sustainable CPA. A series of challenges posed unique hurdles, including:</p>
    <ul>
        <li>Poorly structured Google Ads account</li>
        <li>Subpar ad quality score</li>
        <li>Inadequate ad relevance</li>
        <li>Absence of conversion tracking</li>
        <li>Inoperative pixel across multiple pages</li>
        <li>Overwhelmed server infrastructure</li>
        <li>Outdated theme compromising user experience</li>
        <li>Security vulnerabilities linked to various plugins</li>
        <li>Dynamic Search Ads triggering excessive 404 errors</li>
        <li>Non-functioning forms across several pages</li>
        <li>Diverse and incompatible form technologies complicating matters</li>
        <li>Unexplained deletion of the "Thank You" page</li>
        <li>Flawed setup of Conversions API</li>
    </ul>

    <h2>Strategic Approach</h2>
    <p>The journey commenced with a strategic pause on all campaigns, paving the way for a meticulously crafted Dynamic Search campaign targeting service-specific URLs. Collaborating with a developer, a comprehensive overhaul of the website's technical aspects was undertaken. Compatibility challenges among the CMS, theme, and plugins posed persistent obstacles that demanded unwavering determination.</p>
    <p>This paved the way for the orchestration of a multifaceted digital advertising strategy. A symphony of placements, networks, and platforms converged, supported by a captivating blend of <strong>70%</strong> static images and <strong>30%</strong> dynamic video content. While Search Ads, Display Ads, Discovery Ads, and YouTube Ads contributed significantly to achieving a sustainable CPA, META Ads emerged as a true catalyst for growth. Notably, Instagram Ads played a pivotal role, swiftly generating a substantial influx of leads within a condensed timeframe.</p>

    <h2>Channels Deployed</h2>
    <ul>
        <li>Google Search</li>
        <li>Google Discovery</li>
        <li>Google Display</li>
        <li>Google Audio Ads</li>
        <li>YouTube Ads</li>
        <li>Facebook Ads</li>
        <li>Instagram Ads</li>
        <li>TikTok Ads</li>
        <li>Programmatic Ads</li>
    </ul>
